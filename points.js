import Point from './Point.js';
import Segment from './Segment.js';

const reverse = (segment) => {
    const oldBegin = segment.beginPoint;
    const oldEnd = segment.endPoint;

    const newBegin = new Point(oldEnd.x, oldEnd.y);
    const newEnd = new Point(oldBegin.x, oldBegin.y);
    return new Segment(newBegin, newEnd);
};

export default reverse;

